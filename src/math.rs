pub fn mul_mat4(a: &[[f32; NM]; NM], b: &[[f32; NM]; NM]) -> [[f32; NM]; NM]
{
	let mut c = [[0.0; NM]; NM];
	for i in 0 .. NM
	{
		for j in 0 .. NM
		{
			for k in 0 .. NM
			{
				c[i][j] += a[i][k] * b[k][j];
			}
		}
	}
	c
}

